class Video {
  final String videokey;
  final String name;
  final String type;
  final String language;
  final String site;
  final bool official;
  final int size;

  Video({
    required this.videokey,
    required this.name,
    required this.type,
    required this.language,
    required this.site,
    required this.official,
    required this.size,
  });
}
